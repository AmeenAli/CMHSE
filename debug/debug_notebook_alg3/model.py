class EncoderText(nn.Module):

    def __init__(self, vocab_size, word_dim, embed_size, num_layers,
                 use_abs=False):
        super(EncoderText, self).__init__()
        self.use_abs = use_abs
        self.embed_size = embed_size

        # word embedding
        self.embed = nn.Embedding(vocab_size, word_dim)

        # caption embedding
        self.rnn = nn.GRU(word_dim, embed_size, num_layers, batch_first=True)

        self.init_weights()

    def init_weights(self):
#        embed()
#        self.embed.weight.data.uniform_(-0.1, 0.1)
        coco_glove = torch.from_numpy(np.load('vocab/anet_precomp_w2v.npz')['arr_0'].astype(float)).float()
        self.embed.weight.data = coco_glove

    def forward(self, x, lengths):
        """Handles variable size captions
        """
        # Embed word ids to vectors
        x = self.embed(x)
        packed = pack_padded_sequence(x, lengths, batch_first=True)

        # Forward propagate RNN
        out, _ = self.rnn(packed)

        # Reshape *final* output to (batch_size, hidden_size)
        padded = pad_packed_sequence(out, batch_first=True)
        I = torch.LongTensor(lengths).view(-1, 1, 1)
        I = Variable(I.expand(x.size(0), 1, self.embed_size)-1).cuda()
        out = torch.gather(padded[0], 1, I).squeeze(1)

        # normalization in the joint embedding space
        out = l2norm(out)

        # take absolute value, used by order embeddings
        if self.use_abs:
            out = torch.abs(out)

        return out


def cosine_sim(im, s):
    """Cosine similarity between all the image and sentence pairs
    """
    return im.mm(s.t())


def order_sim(im, s):
    """Order embeddings similarity measure $max(0, s-im)$
    """
    YmX = (s.unsqueeze(1).expand(s.size(0), im.size(0), s.size(1))
           - im.unsqueeze(0).expand(s.size(0), im.size(0), s.size(1)))
    score = -YmX.clamp(min=0).pow(2).sum(2).sqrt().t()
    return score


class ContrastiveLoss(nn.Module):
    """
    Compute contrastive loss
    """

    def __init__(self, margin=0, measure=False, max_violation=False):
        super(ContrastiveLoss, self).__init__()
        self.margin = margin
        if measure == 'order':
            self.sim = order_sim
        else:
            self.sim = cosine_sim

        self.max_violation = max_violation

    def forward(self, im, s):
        # compute image-sentence score matrix
        scores = self.sim(im, s)
        diagonal = scores.diag().view(im.size(0), 1)
        d1 = diagonal.expand_as(scores)
        d2 = diagonal.t().expand_as(scores)

        # compare every diagonal score to scores in its column
        # caption retrieval
        cost_s = (self.margin + scores - d1).clamp(min=0)
        # compare every diagonal score to scores in its row
        # image retrieval
        cost_im = (self.margin + scores - d2).clamp(min=0)

        # clear diagonals
        mask = torch.eye(scores.size(0)) > .5
        I = Variable(mask)
        if torch.cuda.is_available():
            I = I.cuda()
        cost_s = cost_s.masked_fill_(I, 0)
        cost_im = cost_im.masked_fill_(I, 0)

        # keep the maximum violating negative for each query
        if self.max_violation:
            cost_s = cost_s.max(1)[0]
            cost_im = cost_im.max(0)[0]

        return cost_s.sum() + cost_im.sum()


class VSE(object):
    """
    rkiros/uvs model
    """

    def __init__(self, opt):
        # tutorials/09 - Image Captioning
        # Build Models
        self.grad_clip = opt.grad_clip
        self.img_enc = EncoderImage(opt.data_name, opt.img_dim, opt.embed_size,
                                    use_abs=opt.use_abs,
                                    no_imgnorm=opt.no_imgnorm)
        self.txt_enc = EncoderText(opt.vocab_size, opt.word_dim,
                                   opt.embed_size, opt.num_layers,
                                   use_abs=opt.use_abs)
        if torch.cuda.is_available():
            self.img_enc.cuda()
            self.txt_enc.cuda()
            cudnn.benchmark = True

        # Loss and Optimizer
        self.criterion = ContrastiveLoss(margin=opt.margin,
                                         measure=opt.measure,
                                         max_violation=opt.max_violation)
        params = list(self.txt_enc.parameters())
        params += list(self.img_enc.fc.parameters())
        self.params = params

        self.optimizer = torch.optim.Adam(params, lr=opt.learning_rate)

        self.Eiters = 0

    def state_dict(self):
        state_dict = [self.img_enc.state_dict(), self.txt_enc.state_dict()]
        return state_dict

    def load_state_dict(self, state_dict):
        self.img_enc.load_state_dict(state_dict[0])
        self.txt_enc.load_state_dict(state_dict[1])

    def train_start(self):
        """switch to train mode
        """
        self.img_enc.train()
        self.txt_enc.train()

    def val_start(self):
        """switch to evaluate mode
        """
        self.img_enc.eval()
        self.txt_enc.eval()

    def forward_emb(self, video_whole, video_part, captions_whole, captions_part, lengths_whole, lengths_part, volatile=False):
        """Compute the image and caption embeddings
        """
        # Set mini-batch dataset
        video_whole = Variable(video_whole, volatile=volatile)
        video_part = Variable(video_part, volatile=volatile)
        captions_whole = Variable(captions_whole, volatile=volatile)
        captions_part = Variable(captions_part, volatile=volatile)
        if torch.cuda.is_available():
            video_whole = video_whole.cuda()
            video_part = video_part.cuda()
            captions_whole = captions_whole.cuda()
            captions_part = captions_part.cuda()

        # Forward
        vid_whole_emb = self.img_enc(video_whole)
        vid_part_emb = self.img_enc(video_part)
        vid_emb = torch.cat((vid_whole_emb, vid_part_emb), 1)

        lengths_whole_total = [(l, i) for i,l in enumerate(lengths_whole)]
        lengths_whole_total.sort(key=lambda x: x[0], reverse=True)
        lengths_whole_sort = [x[0] for x in lengths_whole_total]
        lengths_whole_sort_id = [x[1] for x in lengths_whole_total]
        captions_whole_sort = captions_whole[lengths_whole_sort_id, :]

        cap_whole_emb = self.txt_enc(captions_whole_sort, lengths_whole_sort)

        cap_whole_emb = cap_whole_emb[np.argsort(lengths_whole_sort_id).tolist(), :]

        lengths_part_total = [(l, i) for i,l in enumerate(lengths_part)]
        lengths_part_total.sort(key=lambda x: x[0], reverse=True)
        lengths_part_sort = [x[0] for x in lengths_part_total]
        lengths_part_sort_id = [x[1] for x in lengths_part_total]
        captions_part_sort = captions_part[lengths_part_sort_id, :]

        cap_part_emb = self.txt_enc(captions_part_sort, lengths_part_sort)

        cap_part_emb = cap_part_emb[np.argsort(lengths_part_sort_id).tolist(), :]


#        cap_part_emb = self.txt_enc(captions_part, lengths_part)
        cap_emb = torch.cat((cap_whole_emb, cap_part_emb), 1)

        vid_emb = l2norm(vid_emb)
        cap_emb = l2norm(cap_emb)
        return vid_emb, cap_emb

    def forward_loss(self, img_emb, cap_emb, **kwargs):
        """Compute the loss given pairs of image and caption embeddings
        """
        loss = self.criterion(img_emb, cap_emb)
        self.logger.update('Le', loss.data[0], img_emb.size(0))
        return loss

    def train_emb(self, video_whole, video_part, captions_whole, captions_part, lengths_whole, lengths_part, ids=None, *args):
        """One training step given images and captions.
        """
        self.Eiters += 1
        self.logger.update('Eit', self.Eiters)
        self.logger.update('lr', self.optimizer.param_groups[0]['lr'])

        # compute the embeddings
        img_emb, cap_emb = self.forward_emb(video_whole, video_part, captions_whole, captions_part, lengths_whole, lengths_part)

        # measure accuracy and record loss
        self.optimizer.zero_grad()
        loss = self.forward_loss(img_emb, cap_emb)

        # compute gradient and do SGD step
        loss.backward()
        if self.grad_clip > 0:
            clip_grad_norm(self.params, self.grad_clip)
        self.optimizer.step()
